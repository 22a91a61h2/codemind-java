public class sum_of_int_Using_Functions {
    static int addnum(int n1,int n2,int n3,int n4,int n5) {
        return n1+n2+n3+n4+n5;
        }
        public static void main(String[] args)
        {
          int result=addnum(10,20,30,40,50);
          System.out.println(result);
        }
    }
