public class Printing {
    public static void main(String[] args)
    {
        int a = 10;
        double b = 12.2;
        String s = "Hello";
        float x = 12.7f;
        byte d = 3;
        char c = 'a';
        System.out.println(a);
        System.out.println(b);
        System.out.println(s);
        System.out.println(x);
        System.out.println(d);
        System.out.println(c);
    }
}
